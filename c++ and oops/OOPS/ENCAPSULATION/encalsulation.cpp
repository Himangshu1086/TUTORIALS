/*
    Encapsulation is defined as wrapping up of data and information under a single unit. 
    In Object Oriented Programming, Encapsulation is defined as 
    binding together the data and the functions that manipulates them.

    Consider a real life example of encapsulation, in a company there are different sections 
    like the accounts section, finance section, sales section etc. 
    The finance section handles all the financial transactions and keep records 
    of all the data related to finance. Similarly the sales section handles all 
    the sales related activities and keep records of all the sales. 
    Now there may arise a situation when for some reason an official from finance 
    section needs all the data about sales in a particular month. 
    In this case, he is not allowed to directly access the data of sales section. 
    He will first have to contact some other officer in the sales section and 
    then request him to give the particular data. This is what encapsulation is. 
    Here the data of sales section and the employees that can manipulate them are 
    wrapped under a single name “sales section”.

    In C++ encapsulation can be implemented using Class and access modifiers. 
*/