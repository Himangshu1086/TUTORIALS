print("basic calculator")
a = float(input("Enter a number : \t"))
b = float(input('Enter a number : \t'))
c = str(input("Enter the operation : \t"))
if c=="+":
    print("Result = ", a+b)
elif c=="-":
    print("Result = ", a-b)
elif c=="*":
    print("Result = ", a*b)
elif c=="/":
    print("Result = ", a/b)




